/**
 * Read Data/Day 2 time slots.csv → update local xlsx + Day2Seed.gs for Bootstrap Option A.
 * Run: npm run sync:day2  then  clasp push  then  Bootstrap Option A on the live sheet.
 */
const fs = require('fs');
const path = require('path');
const xlsx = require('xlsx');
const {
  DAY2_SHEET_NAME,
  ROOM_CONFIG_HEADERS,
  resolveCdPdSourcePath,
  readDay2GridRows,
  normalizeDay2GridDates,
  parseDay2Records,
  uniqueRoomStations,
  readExistingStationNames,
  compareStationLists,
  summarizeRecords
} = require('./day2-schedule-shared');

const SYSTEM_PATH = path.join('Data', 'PD System (2).xlsx');

function upsertSheet(workbook, name, sheet) {
  workbook.Sheets[name] = sheet;
  if (!workbook.SheetNames.includes(name)) workbook.SheetNames.push(name);
}

function main() {
  const sourcePath = resolveCdPdSourcePath();
  const { day2Rows: rawRows, sheetName } = readDay2GridRows(sourcePath);
  const day2Rows = normalizeDay2GridDates(rawRows);
  const records = parseDay2Records(day2Rows);
  const summary = summarizeRecords(records);
  const newRoomStations = uniqueRoomStations(records);
  const priorStations = readExistingStationNames(SYSTEM_PATH);
  const stationDiff = compareStationLists(
    priorStations.filter((n) => n.startsWith('Room ')),
    newRoomStations
  );

  let systemWb;
  if (fs.existsSync(SYSTEM_PATH)) {
    systemWb = xlsx.readFile(SYSTEM_PATH);
  } else {
    systemWb = xlsx.utils.book_new();
  }

  upsertSheet(systemWb, DAY2_SHEET_NAME, xlsx.utils.aoa_to_sheet(day2Rows));

  const roomRows = records.map((rec) => [
    rec.campus,
    rec.room,
    rec.sessionName,
    rec.pdDate,
    rec.startTime,
    rec.endTime,
    rec.active,
    rec.stationName,
    rec.notes
  ]);
  upsertSheet(systemWb, 'RoomConfig', xlsx.utils.aoa_to_sheet([ROOM_CONFIG_HEADERS, ...roomRows]));

  const stationNames = {};
  for (const s of ['FrontOffice', 'Cafeteria', 'Gym', 'Main Check In']) stationNames[s] = 'Scan';
  for (const rec of records) {
    if (rec.active && rec.stationName) stationNames[rec.stationName] = 'ID';
  }

  const stationAoa = [['Station Name', 'Enabled', 'Notes', 'Web App URL', 'Entry Mode']];
  for (const name of Object.keys(stationNames).sort()) {
    stationAoa.push([
      name,
      true,
      name.startsWith('Room ') ? 'Day 2 session room' : 'Day 1 / main check-in',
      '',
      stationNames[name]
    ]);
  }
  upsertSheet(systemWb, 'Stations', xlsx.utils.aoa_to_sheet(stationAoa));

  let settingsRows = [['Setting', 'Value', 'Notes']];
  if (systemWb.Sheets.Settings) {
    settingsRows = xlsx.utils.sheet_to_json(systemWb.Sheets.Settings, { header: 1, defval: '' });
    if (!settingsRows[0] || settingsRows[0][0] !== 'Setting') {
      settingsRows = [['Setting', 'Value', 'Notes'], ...settingsRows];
    }
  }
  const hasSummerId = settingsRows.some((r) => String(r[0]).trim() === 'Summer PD Plan Spreadsheet ID');
  if (!hasSummerId) {
    settingsRows.push([
      'Summer PD Plan Spreadsheet ID',
      '',
      'Leave blank when Day 2 time slots tab is in this spreadsheet.'
    ]);
  }
  upsertSheet(systemWb, 'Settings', xlsx.utils.aoa_to_sheet(settingsRows));

  xlsx.writeFile(systemWb, SYSTEM_PATH);

  const seedPath = path.join('Day2Seed.gs');
  const json = JSON.stringify(day2Rows);
  const gs = `/** Auto-generated by scripts/apply-option-a.js — do not edit by hand. */\nconst DAY2_SEED_DATA_ = ${json};\n`;
  fs.writeFileSync(seedPath, gs, 'utf8');

  console.log('Source:', sourcePath, '(' + sheetName + ' tab)');
  console.log('Wrote', SYSTEM_PATH);
  console.log('  Grid rows:', day2Rows.length);
  console.log('  RoomConfig session slots:', summary.sessionSlots);
  console.log('  Room stations:', summary.roomStations);
  console.log('  Campuses:', summary.campuses.join(', '));
  console.log('  By date:', summary.byDate);
  console.log('  Stations total:', stationAoa.length - 1);
  if (stationDiff.added.length) console.log('  New room stations:', stationDiff.added.join(', '));
  if (stationDiff.removed.length) console.log('  Removed room stations:', stationDiff.removed.join(', '));
  console.log('Wrote', seedPath, '(' + Math.round(json.length / 1024) + ' KB)');
  console.log('\nNext: clasp push → PD Scanner → Bootstrap Option A → Build Station URLs');
}

main();
